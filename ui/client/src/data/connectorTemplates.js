// Group templates by category for the UI
export function getConnectorsByCategory() {
  const categories = {};
  for (const connector of CONNECTOR_TEMPLATES) {
    if (!categories[connector.category]) {
      categories[connector.category] = [];
    }
    categories[connector.category].push(connector);
  }
  return categories;
}

// Search connectors by name or description
export function searchConnectors(query) {
  if (!query) return CONNECTOR_TEMPLATES;
  const q = query.toLowerCase();
  return CONNECTOR_TEMPLATES.filter(
    (c) =>
      c.name.toLowerCase().includes(q) ||
      c.description.toLowerCase().includes(q) ||
      c.category.toLowerCase().includes(q)
  );
}

